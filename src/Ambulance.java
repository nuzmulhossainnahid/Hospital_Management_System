    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        div.setText(jComboBox1.getSelectedItem().toString());
        dist.setText(jComboBox2.getSelectedItem().toString());

        JOptionPane.showMessageDialog(rootPane, "Division: "+jComboBox1.getSelectedItem().toString()+"\n District: "
            +jComboBox2.getSelectedItem().toString());

        String Divition = div.getText();
        String Dist = dist.getText();

        if(Divition.equals("RAJSHAHI") && Dist.equals("Joypurhat District")){
            Ambulance_joypurhat obj = new Ambulance_joypurhat();
            obj.setVisible(true);
            dispose();
        }

        else if(Divition.equals("CHITTAGONG") && Dist.equals("Cumilla District")){
            Ambulance_comilla obj = new Ambulance_comilla();
            obj.setVisible(true);
            dispose();
        }

        else{
            JOptionPane.showMessageDialog(rootPane, "SORRY!  I NEED TO UPDATE");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

